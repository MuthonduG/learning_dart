void main() {
    // Strings
    String firstName = "John";
    print("$firstName");

    // integer and double
    int num = 45;
    double myDouble = 45.5;
    print("$num");
    print("$myDouble");

    // boolean
    bool myBool = false;
    print("$myBool");

    // dynamic
    dynamic nameTwo = "John Doe";
    dynamic z = 42;

    print("$z and $nameTwo");

}